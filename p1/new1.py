#Bitwise Operator 
a = 5
b = 3

# Bitwise AND
result_and = a & b
print(6&5)
# Bitwise OR
result_or = a | b
print(5|3)
# Bitwise XOR
result_xor = a ^ b
print(5^4)
# Bitwise NOT
result_not = ~a
print(~5)
# Bitwise left shift
result_left_shift = a << 2
print(5<<2)
# Bitwise right shift
result_right_shift = a >> 2
print(5>>2)
